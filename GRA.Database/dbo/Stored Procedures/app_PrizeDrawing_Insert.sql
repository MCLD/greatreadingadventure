
--Create the Insert Proc
CREATE PROCEDURE [dbo].[app_PrizeDrawing_Insert] (
	@PrizeName VARCHAR(250),
	@TID INT,
	@DrawingDateTime DATETIME,
	@NumWinners INT,
	@LastModDate DATETIME,
	@LastModUser VARCHAR(50),
	@AddedDate DATETIME,
	@AddedUser VARCHAR(50),
	@TenID INT = 0,
	@FldInt1 INT = 0,
	@FldInt2 INT = 0,
	@FldInt3 INT = 0,
	@FldBit1 BIT = 0,
	@FldBit2 BIT = 0,
	@FldBit3 BIT = 0,
	@FldText1 TEXT = '',
	@FldText2 TEXT = '',
	@FldText3 TEXT = '',
	@PDID INT OUTPUT
	)
AS
BEGIN
	INSERT INTO PrizeDrawing (
		PrizeName,
		TID,
		DrawingDateTime,
		NumWinners,
		LastModDate,
		LastModUser,
		AddedDate,
		AddedUser,
		TenID,
		FldInt1,
		FldInt2,
		FldInt3,
		FldBit1,
		FldBit2,
		FldBit3,
		FldText1,
		FldText2,
		FldText3
		)
	VALUES (
		@PrizeName,
		@TID,
		@DrawingDateTime,
		@NumWinners,
		@LastModDate,
		@LastModUser,
		@AddedDate,
		@AddedUser,
		@TenID,
		@FldInt1,
		@FldInt2,
		@FldInt3,
		@FldBit1,
		@FldBit2,
		@FldBit3,
		@FldText1,
		@FldText2,
		@FldText3
		)

	SELECT @PDID = SCOPE_IDENTITY()
END
