
CREATE PROCEDURE [dbo].[app_LibraryCrosswalk_Insert] (
	@BranchID INT,
	@DistrictID INT,
	@City VARCHAR(50),
	@TenID INT = 0,
	@FldInt1 INT = 0,
	@FldInt2 INT = 0,
	@FldInt3 INT = 0,
	@FldBit1 BIT = 0,
	@FldBit2 BIT = 0,
	@FldBit3 BIT = 0,
	@FldText1 TEXT = '',
	@FldText2 TEXT = '',
	@FldText3 TEXT = '',
	@ID INT OUTPUT
	)
AS
BEGIN
	INSERT INTO LibraryCrosswalk (
		BranchID,
		DistrictID,
		City,
		TenID,
		FldInt1,
		FldInt2,
		FldInt3,
		FldBit1,
		FldBit2,
		FldBit3,
		FldText1,
		FldText2,
		FldText3
		)
	VALUES (
		@BranchID,
		@DistrictID,
		@City,
		@TenID,
		@FldInt1,
		@FldInt2,
		@FldInt3,
		@FldBit1,
		@FldBit2,
		@FldBit3,
		@FldText1,
		@FldText2,
		@FldText3
		)

	SELECT @ID = SCOPE_IDENTITY()
END
