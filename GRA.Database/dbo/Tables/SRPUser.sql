    [FldBit3]           BIT           NULL,
    [FldText1]          TEXT          NULL,
    [FldText2]          TEXT          NULL,
    [FldText3]          TEXT          NULL,
    CONSTRAINT [PK_SRPUser] PRIMARY KEY CLUSTERED ([UID] ASC)
);

