    [IsDeleted]         BIT           NULL,
    [LastPasswordReset] DATETIME      NULL,
    [DeletedDate]       DATETIME      NULL,
    [LastModDate]       DATETIME      CONSTRAINT [DF_SRPUser_LastModDate] DEFAULT (getdate()) NULL,
    [LastModUser]       VARCHAR (50)  CONSTRAINT [DF_SRPUser_LastModUser] DEFAULT ('N/A') NULL,
    [AddedDate]         DATETIME      CONSTRAINT [DF_SRPUser_AddedDate] DEFAULT (getdate()) NULL,
